# arithmetic operators
# operator   meaning
# +          addition
x = 2 + 3
# -          subtraction
x = 5 - 2
# *          multiplication
x = 3 * 4
# /          division
x = 8 / 2
# %          modulus (remainder)
x = 7 % 3
# **         exponentiation (power)
x = 2 ** 3
# //         floor division
x = 9 // 2

#comparison operators
# operator    meaning
# ==          equal to
print(x == 2)
# !=          not equal to
print(x != 3)
# >           greater than
print(x > 2)
# <           less than
print(x < 3)
# >=          greater than or equal to
print(x >= 2)
# <=          less than or equal to
print(x <= 3)

# assignment operators
# operator     meaning
# +=           add and assign
x += 2
# -=           subtract and assign
x -= 2
# *=           multiply and assign
x *= 2
# /=           divide and assign
x /= 2
# %=           modulus and assign
x %= 2
# **=          exponentiate and assign
x **= 2
# //=          floor divide and assign
x //= 2
# =            assign value
x = 5

# example
x = 5
x += 2
print(x)  # Output: 7

