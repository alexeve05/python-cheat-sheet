# Variables

# Variables are used to store data that can be referenced and manipulated later.
# Think of them as “containers” for information.

# int : integers (whole numbers)
# age = 20

# float : decimal number 
# pi_value = 3.14

# str: string (text)
# firstName = "Alice"

# boolean: true or false
# inStudent = True

# constants (by covnention, all caps)
# MAX_SIZE = 100

# Variable naming rules:
# Use clear, descriptive variable names
# should not start with number
# must start with a letter or underscore
# case senstitive (age and Age are different variables)
# snake_case or camelCase
# cannot use reserved keywords (like print, if, else, etc.)