# Type Casting
# converting one data type to another

# String to number
numStr = "16"
num = int(numStr)
print(num + 2)

# Float conversion
value = "3.14"
pi = float(value)
print(pi)

# Number to string
age = 21
text = str(age)
print("Age: " + text)

# Boolean conversion
print(bool(1))
print(bool(0))
print(bool(""))
print(bool("hi"))

# Common input
age = int(input("Enter your age: "))
print(age + 1)

# input() will always return a string.